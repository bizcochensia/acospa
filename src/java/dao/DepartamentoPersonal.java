package dao;

public class DepartamentoPersonal {
  int id;
  String departamento_id;
  String personal_numEmpleado;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDepartamento_id() {
        return departamento_id;
    }

    public void setDepartamento_id(String departamento_id) {
        this.departamento_id = departamento_id;
    }

    public String getPersonal_numEmpleado() {
        return personal_numEmpleado;
    }

    public void setPersonal_numEmpleado(String personal_numEmpleado) {
        this.personal_numEmpleado = personal_numEmpleado;
    }
  
}
